public class CaicDemo {
    /**
     *加法
     * @param a
     * @param b
     * @return
     */
    public int add(int a,int b){
        return a+b;
    }

    /**
     * 减法
     * @param a
     * @param b
     * @return
     */
    public int sub(int a,int b){
        return a-b;
    }

    /**
     * 乘法
     * @param a
     * @param b
     * @return
     */
    public int multi(int a,int b){
        return a*b;
    }

    /**
     * 除法
     * @param a
     * @param b
     * @return
     */
    public double div(double a,double b){
        return a/b;
    }
}
